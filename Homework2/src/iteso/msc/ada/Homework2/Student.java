package iteso.msc.ada.Homework2;

public class Student {
	public int ID;
	public String Name;
	public int Age;
	public String Program;

	public Student() {}
	
	public Student(int id, String name, int age, String program) {
		this.ID = id;
		this.Name = name;
		this.Age = age;
		this.Program = program;
	}
}
